function handleMeeting(event) {
    event.preventDefault();

    // Get form data
    const meetingTitle = document.getElementById("meetingTitle").value;
    const meetingDate = document.getElementById("meetingDate").value;
    const meetingTime = document.getElementById("meetingTime").value;
    const agenda = document.getElementById("agenda").value;
    const meetingURL = document.getElementById("meetingURL").value;
    console.log("Meeting URL:", meetingURL);

// Create an object with meeting information
    const meeting = {
        title: meetingTitle,
        scheduledDateTime: `${meetingDate}T${meetingTime}:00`,
        description: agenda,
        participants: [], // You may add participants if needed
        SessionURL: meetingURL, // Include the Meeting URL field
        id: null, // You may set the ID to null if it's autogenerated on the server
    };


    // Send a POST request to save the meeting
    fetch('http://localhost:8080/api/v1/users/saveMeeting', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
        },
        body: JSON.stringify(meeting),
    })
        .then(response => {
            if (!response.ok) {
                throw new Error('Network response was not ok');
            }
            return response;
        })
        .then(() => {
            // Redirect to "index.html" upon successful save
            window.location.href = "index.html";
        })
        .catch(error => {
            console.error('POST request error:', error);
            // Handle errors, show a message, etc.
        });
}

// Attach the handleMeeting function to the form's submit event
const meetingForm = document.getElementById("meetingForm");
meetingForm.addEventListener("submit", handleMeeting);
